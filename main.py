def _set_status_indicator(text: str, color: str):
    if _tk_root is None or _sv_status is None or _status_label is None:
        return
    def _update():
        if _sv_status: _sv_status.set(text)
        if _status_label: _status_label.configure(text_color=color)
    _tk_root.after(0, _update)


def _update_status_indicator():
    """Inspect token state and refresh the header badge accordingly."""
    rt = keyring.get_password(KEYRING_SERVICE, KEYRING_REFRESH_KEY)
    if not rt:
        _set_status_indicator("● Not paired", RED)
        return
    if _id_token and time.time() < _token_expiry:
        _set_status_indicator("● Connected", GREEN)
        return
    _set_status_indicator("● Reconnecting…", YELLOW)
    try:
        get_valid_token()
        _set_status_indicator("● Connected", GREEN)
    except Exception:
        _set_status_indicator("● Auth error", RED)


def _status_watcher():
    while True:
        try:
            _update_status_indicator()
        except Exception:
            pass
        time.sleep(30)


def _open_settings(root: ctk.CTk):
    global _config, _current_hotkey, _current_cancel_hotkey
    root.withdraw()
    new_cfg = run_settings_dialog(_config)
    if not new_cfg:
        root.deiconify()
        return

    # Re-pair if requested
    if new_cfg.pop("_repair", False):
        paired = run_pairing_dialog(root)
        if not paired:
            root.deiconify()
            return
        new_cfg["uid"] = paired["uid"]

    # Hotkey rebind
    new_hotkey = new_cfg.get("hotkey", "f9")
    new_cancel = new_cfg.get("cancel_hotkey", "")
    if new_hotkey != _current_hotkey:
        try:
            keyboard.remove_hotkey(_current_hotkey)
        except Exception:
            pass
        keyboard.add_hotkey(new_hotkey, on_hotkey, suppress=False)
        _current_hotkey = new_hotkey
        if _sv_instructions is not None:
            _sv_instructions.set(_format_instructions(new_hotkey))
    if new_cancel != _current_cancel_hotkey:
        if _current_cancel_hotkey:
            try:
                keyboard.remove_hotkey(_current_cancel_hotkey)
            except Exception:
                pass
        if new_cancel:
            try:
                keyboard.add_hotkey(new_cancel, on_cancel_hotkey, suppress=False)
            except Exception as e:
                messagebox.showwarning("Hotkey",
                    f"Couldn't bind cancel hotkey '{new_cancel}': {e}")
                new_cancel = ""
        _current_cancel_hotkey = new_cancel
        new_cfg["cancel_hotkey"] = new_cancel

    save_config(new_cfg)
    _config = new_cfg
    _rebuild_tray_menu()
    _update_status_indicator()
    root.deiconify()


# ── Pairing dialog ─────────────────────────────────────────────────────────────

def run_pairing_dialog(parent=None) -> "dict | None":
    """Modal dialog: shows pairing code, polls until claimed.
    Returns {uid, idToken, refreshToken} on success, or None on cancel/expiry.
    """
    global _id_token, _token_expiry

    dlg = ctk.CTkToplevel(parent) if parent else ctk.CTk()
    dlg.title("HeroIntel Capture — Pair desktop")
    dlg.geometry("520x460")
    dlg.resizable(False, False)
    dlg.configure(fg_color=BG)
    _apply_window_icon(dlg)

    # Tkinter is not thread-safe — the background pairing thread only mutates
    # this dict; the dialog drains pending UI updates from it via dlg.after().
    state_lock = threading.Lock()
    state = {
        "result": None,         # set on success: {"uid": ...}
        "stop": False,          # user clicked Cancel
        "pairing": None,        # current pairing init payload, or None to request a new code
        "pending_ui": None,     # next (text, color, code) to apply on the main thread
        "done": False,          # success — schedule destroy
        "abort_thread": False,  # tells the worker thread to exit without touching tk
    }

    # ── Header with logo ──
    hdr = ctk.CTkFrame(dlg, fg_color=BG_RAISED, corner_radius=0, height=72)
    hdr.pack(fill="x")
    hdr.pack_propagate(False)
    logo_img = _get_wordmark_image(44)
    if logo_img is not None:
        ctk.CTkLabel(hdr, image=logo_img, text="").pack(side="left", padx=(20, 14), pady=12)
    ctk.CTkLabel(hdr, text="Pair desktop", text_color=BLUE,
                 font=("Segoe UI", 18, "bold")).pack(side="left", pady=14)

    ctk.CTkLabel(dlg, text="Enter this code on the web app to connect this device:",
                 text_color=FG_DIM, font=("Segoe UI", 12), wraplength=480,
                 justify="left").pack(anchor="w", padx=20, pady=(20, 8))

    code_var = tk.StringVar(value="…")
    code_lbl = ctk.CTkLabel(dlg, textvariable=code_var, text_color=FG,
                            font=("Consolas", 36, "bold"))
    code_lbl.pack(pady=(8, 6))

    status_var = tk.StringVar(value="Requesting code…")
    status_lbl = ctk.CTkLabel(dlg, textvariable=status_var, text_color=FG_DIM,
                              font=("Segoe UI", 11))
    status_lbl.pack(pady=(0, 16))

    def copy_code():
        code = code_var.get()
        if code and code != "…":
            dlg.clipboard_clear()
            dlg.clipboard_append(code)

    def open_browser():
        webbrowser.open(_pairing_url("/pair"))

    btn_row = ctk.CTkFrame(dlg, fg_color=BG, corner_radius=0)
    btn_row.pack(fill="x", padx=20, pady=4)
    ctk.CTkButton(btn_row, text="Open pairing page", command=open_browser,
                  fg_color=ORANGE, hover_color="#e07010", text_color="white",
                  font=("Segoe UI", 12, "bold"), corner_radius=8, height=42).pack(
        side="left", expand=True, fill="x", padx=(0, 6))
    ctk.CTkButton(btn_row, text="Copy code", command=copy_code,
                  fg_color=BG_CARD, hover_color=BG_INPUT, text_color=FG_DIM,
                  font=("Segoe UI", 12), corner_radius=8, height=42).pack(
        side="left", expand=True, fill="x", padx=(6, 0))

    def cancel():
        with state_lock:
            state["stop"] = True
            state["abort_thread"] = True
        dlg.destroy()

    def request_new_code():
        with state_lock:
            state["pairing"] = None
            state["abort_thread"] = True  # tell current worker to exit
        code_var.set("…")
        status_var.set("Requesting code…")
        # Spawn fresh worker after a tick so the old one can notice abort.
        def _spawn():
            with state_lock:
                state["abort_thread"] = False
            threading.Thread(target=_do_pair, daemon=True).start()
        dlg.after(200, _spawn)

    foot = ctk.CTkFrame(dlg, fg_color=BG, corner_radius=0)
    foot.pack(fill="x", padx=20, pady=(12, 16))
    ctk.CTkButton(foot, text="Get a new code", command=request_new_code,
                  fg_color=BG_CARD, hover_color=BG_INPUT, text_color=FG_DIM,
                  font=("Segoe UI", 11), corner_radius=8, height=36).pack(side="left")
    ctk.CTkButton(foot, text="Cancel", command=cancel,
                  fg_color=BG_CARD, hover_color=BG_INPUT, text_color=FG_DIM,
                  font=("Segoe UI", 11), corner_radius=8, height=36).pack(side="right")

    def _post(text, color=None, code=None):
        """Thread-safe: queues a UI update for the main-thread poller to apply."""
        with state_lock:
            state["pending_ui"] = (text, color, code)

    def _drain_ui():
        """Runs on the main thread via dlg.after — applies queued updates."""
        try:
            with state_lock:
                update = state["pending_ui"]
                state["pending_ui"] = None
                done = state["done"]
            if update is not None:
                text, color, code = update
                if code is not None:
                    code_var.set(code)
                status_var.set(text)
                if color is not None:
                    status_lbl.configure(text_color=color)
            if done:
                dlg.after(900, dlg.destroy)
                return
            if state["stop"]:
                return
            dlg.after(120, _drain_ui)
        except tk.TclError:
            # Dialog destroyed — bail out silently.
            return

    def _do_pair():
        global _id_token, _token_expiry
        try:
            init = initiate_pairing()
        except Exception as e:
            _post(f"Could not request a code: {e}", RED)
            return
        with state_lock:
            if state["abort_thread"]:
                return
            state["pairing"] = init
        _post("Waiting for browser…", FG_DIM, code=init["code"])

        expires = init.get("expiresAt", "")
        try:
            expires_at = datetime.fromisoformat(expires.replace("Z", "+00:00")).timestamp()
        except Exception:
            expires_at = time.time() + 600

        while True:
            with state_lock:
                if state["stop"] or state["abort_thread"]:
                    return
            if time.time() > expires_at:
                _post("Code expired. Get a new one.", YELLOW)
                return
            try:
                res = poll_pairing(init["pairingToken"])
            except Exception:
                time.sleep(2)
                continue
            if res.get("status") == "claimed":
                custom_token = res["customToken"]
                uid = res["uid"]
                try:
                    id_token, refresh_token = sign_in_custom_token(custom_token)
                except Exception as e:
                    _post(f"Sign-in failed: {e}", RED)
                    return
                keyring.set_password(KEYRING_SERVICE, KEYRING_REFRESH_KEY, refresh_token)
                _id_token = id_token
                _token_expiry = time.time() + 3500
                with state_lock:
                    state["result"] = {"uid": uid}
                    state["done"] = True
                _post("Paired ✓", GREEN)
                return
            if res.get("status") == "expired":
                _post("Code expired. Get a new one.", YELLOW)
                return
            time.sleep(2)

    threading.Thread(target=_do_pair, daemon=True).start()
    dlg.after(120, _drain_ui)

    if parent:
        dlg.transient(parent)
        dlg.grab_set()
        _position_near(dlg, parent)
    dlg.wait_window()
    with state_lock:
        state["abort_thread"] = True
    return state["result"]


