# ── Resource path (handles PyInstaller --onefile) ──────────────────────────────

def _resource_path(name: str) -> Path:
    if getattr(sys, "frozen", False):
        return Path(getattr(sys, "_MEIPASS", "")) / name
    return Path(__file__).parent / name


def _claim_app_identity():
    """Windows: register an explicit AppUserModelID so the OS shows our taskbar
    icon (and groups our windows separately from Python.exe). Without this,
    `python main.py` displays the Python interpreter's icon on the taskbar."""
    if sys.platform != "win32":
        return
    try:
        import ctypes
        ctypes.windll.shell32.SetCurrentProcessExplicitAppUserModelID(
            "HeroIntel.Capture")
    except Exception:
        pass


def _get_logo_image(size: int) -> "ctk.CTkImage | None":
    """Return a cached CTkImage of the square brand mark at the given size."""
    key = ("icon", size)
    if key in _logo_image_cache:
        return _logo_image_cache[key]
    path = _resource_path("icon.png")
    if not path.exists():
        return None
    try:
        img = Image.open(path).convert("RGBA")
        ctk_img = ctk.CTkImage(light_image=img, dark_image=img, size=(size, size))
        _logo_image_cache[key] = ctk_img
        return ctk_img
    except Exception:
        return None


def _get_wordmark_image(height: int) -> "ctk.CTkImage | None":
    """Return a cached CTkImage of the wide wordmark logo (logo3) at the given
    height, scaled to preserve aspect ratio."""
    key = ("wordmark", height)
    if key in _logo_image_cache:
        return _logo_image_cache[key]
    path = _resource_path("wordmark.png")
    if not path.exists():
        # Fall back to the square mark so headers still render.
        return _get_logo_image(height)
    try:
        img = Image.open(path).convert("RGBA")
        w, h = img.size
        scaled_w = max(1, int(w * (height / h)))
        ctk_img = ctk.CTkImage(light_image=img, dark_image=img, size=(scaled_w, height))
        _logo_image_cache[key] = ctk_img
        return ctk_img
    except Exception:
        return None


