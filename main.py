_icon_hicon_cache: list = []     # keep HICON handles alive for the process

# Win32 constants
_WM_SETICON      = 0x0080
_ICON_SMALL      = 0
_ICON_BIG        = 1
_ICON_SMALL2     = 2
_IMAGE_ICON      = 1
_LR_LOADFROMFILE = 0x00000010
_LR_DEFAULTSIZE  = 0x00000040


def _win_set_icon(window):
    """Bypass Tcl's iconbitmap (which silently fails inside PyInstaller's
    _MEIPASS temp dir) and send WM_SETICON straight to the HWND."""
    if sys.platform != "win32":
        return
    ico = _resource_path("icon.ico")
    if not ico.exists():
        return
    try:
        import ctypes
        from ctypes import wintypes
        user32 = ctypes.windll.user32

        # Resolve window's HWND. Tk widgets sit inside a frame; the HWND we
        # actually want is the top-level parent of winfo_id().
        hwnd = wintypes.HWND(int(window.winfo_id()))
        # Walk up to the toplevel that owns the taskbar entry.
        GA_ROOT = 2
        get_ancestor = user32.GetAncestor
        get_ancestor.restype = wintypes.HWND
        get_ancestor.argtypes = [wintypes.HWND, ctypes.c_uint]
        top_hwnd = get_ancestor(hwnd, GA_ROOT) or hwnd

        load_image = user32.LoadImageW
        load_image.restype = wintypes.HANDLE
        load_image.argtypes = [
            wintypes.HINSTANCE, wintypes.LPCWSTR, wintypes.UINT,
            ctypes.c_int, ctypes.c_int, wintypes.UINT,
        ]
        send_message = user32.SendMessageW
        send_message.restype = ctypes.c_ssize_t
        send_message.argtypes = [
            wintypes.HWND, wintypes.UINT, ctypes.c_ssize_t, ctypes.c_ssize_t
        ]

        path = str(ico)
        small = load_image(None, path, _IMAGE_ICON, 16, 16, _LR_LOADFROMFILE)
        big   = load_image(None, path, _IMAGE_ICON, 32, 32, _LR_LOADFROMFILE)
        if small:
            _icon_hicon_cache.append(small)
            send_message(top_hwnd, _WM_SETICON, _ICON_SMALL, small)
            send_message(top_hwnd, _WM_SETICON, _ICON_SMALL2, small)
        if big:
            _icon_hicon_cache.append(big)
            send_message(top_hwnd, _WM_SETICON, _ICON_BIG, big)
    except Exception as e:
        print(f"[icon] WM_SETICON failed: {e}")


def _apply_window_icon(window):
    """Set the window/taskbar/title-bar icon. Layered approach:
       1. Tcl iconphoto (works in dev, sets default for child toplevels)
       2. Tcl iconbitmap (Win11 title-bar polish)
       3. Win32 WM_SETICON (the only one that survives PyInstaller --onefile)
    """
    png = _resource_path("icon.png")
    ico = _resource_path("icon.ico")

    if png.exists():
        try:
            from PIL import ImageTk
            img = Image.open(png).convert("RGBA")
            photos = []
            for size in (16, 24, 32, 48, 64):
                photos.append(ImageTk.PhotoImage(img.resize((size, size), Image.LANCZOS)))
            _icon_photo_cache.extend(photos)
            window.iconphoto(True, *photos)
        except Exception as e:
            print(f"[icon] iconphoto failed: {e}")

    if ico.exists():
        try:
            window.iconbitmap(default=str(ico))
        except Exception:
            try:
                window.iconbitmap(str(ico))
            except Exception:
                pass

    # Win32 path — must run after the window has an HWND. We schedule it once
    # immediately and again on <Map> in case the HWND wasn't created yet.
    def _set_now(_evt=None):
        try:
            window.update_idletasks()
        except Exception:
            pass
        _win_set_icon(window)
    window.after(0, _set_now)
    try:
        window.bind("<Map>", _set_now, add="+")
    except Exception:
        pass


def _position_near(dlg, parent):
    """Center `dlg` over `parent` using its requested geometry."""
    if parent is None:
        return
    try:
        parent.update_idletasks()
        dlg.update_idletasks()
        px = parent.winfo_x()
        py = parent.winfo_y()
        pw = parent.winfo_width()
        ph = parent.winfo_height()
        dw = dlg.winfo_reqwidth()
        dh = dlg.winfo_reqheight()
        x = max(0, px + (pw - dw) // 2)
        y = max(0, py + (ph - dh) // 2)
        dlg.geometry(f"+{x}+{y}")
    except Exception:
        pass

# ── Firebase REST auth ─────────────────────────────────────────────────────────

_CUSTOM_TOKEN_URL = "https://identitytoolkit.googleapis.com/v1/accounts:signInWithCustomToken?key={key}"
_REFRESH_URL = "https://securetoken.googleapis.com/v1/token?key={key}"

def sign_in_custom_token(custom_token: str) -> tuple:
    """Exchange a Firebase custom token for an idToken + refreshToken."""
    r = requests.post(
        _CUSTOM_TOKEN_URL.format(key=FIREBASE_API_KEY),
        json={"token": custom_token, "returnSecureToken": True},
        timeout=15,
    )
    r.raise_for_status()
    d = r.json()
    return d["idToken"], d["refreshToken"]

def _refresh(refresh_token: str) -> tuple:
    r = requests.post(
        _REFRESH_URL.format(key=FIREBASE_API_KEY),
        data={"grant_type": "refresh_token", "refresh_token": refresh_token},
        timeout=10,
    )
    r.raise_for_status()
    d = r.json()
    return d["id_token"], time.time() + int(d.get("expires_in", 3600)) - 60

def get_valid_token() -> str:
    global _id_token, _token_expiry
    if _id_token and time.time() < _token_expiry:
        return _id_token
    rt = keyring.get_password(KEYRING_SERVICE, KEYRING_REFRESH_KEY)
    if not rt:
        raise RuntimeError("Not paired. Open Settings → Pair desktop.")
    _id_token, _token_expiry = _refresh(rt)
    return _id_token

# ── Device pairing ─────────────────────────────────────────────────────────────

def _pairing_url(path: str) -> str:
    return WEB_APP_URL.rstrip("/") + path

def initiate_pairing() -> dict:
    r = requests.post(_pairing_url("/api/devices/initiate"), timeout=15)
    r.raise_for_status()
    return r.json()

def poll_pairing(pairing_token: str) -> dict:
    r = requests.get(
        _pairing_url("/api/devices/poll"),
        params={"token": pairing_token},
        timeout=15,
    )
    if r.status_code in (404, 410):
        return {"status": "expired"}
    r.raise_for_status()
    return r.json()

# ── Firebase Storage upload ────────────────────────────────────────────────────

def upload_screenshot(jpeg_bytes: bytes, storage_path: str, token: str) -> str:
    encoded = requests.utils.quote(storage_path, safe="")
    url = f"https://firebasestorage.googleapis.com/v0/b/{FIREBASE_STORAGE_BUCKET}/o?uploadType=media&name={encoded}"
    r = requests.post(url, data=jpeg_bytes,
                      headers={"Authorization": f"Bearer {token}", "Content-Type": "image/jpeg"},
                      timeout=30)
    r.raise_for_status()
    dl_token = r.json().get("downloadTokens", "")
    return f"https://firebasestorage.googleapis.com/v0/b/{FIREBASE_STORAGE_BUCKET}/o/{encoded}?alt=media&token={dl_token}"

# ── Firestore REST ─────────────────────────────────────────────────────────────

_FS_BASE = "https://firestore.googleapis.com/v1/projects/{proj}/databases/(default)/documents"

def _fs_headers(token: str) -> dict:
    return {"Authorization": f"Bearer {token}", "Content-Type": "application/json"}

def create_pending_match(uid: str, rank_url: str, rank_path: str, token: str) -> str:
    url = f"{_FS_BASE.format(proj=FIREBASE_PROJECT_ID)}/users/{uid}/pendingScreenshots"
    now = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    body = {"fields": {
        "rankProgressUrl":  {"stringValue": rank_url},
        "rankProgressPath": {"stringValue": rank_path},
        "scoreboardUrl":    {"nullValue": None},
        "scoreboardPath":   {"nullValue": None},
        "uploadedAt":       {"timestampValue": now},
        "status":           {"stringValue": "waiting_scoreboard"},
        "consumed":         {"booleanValue": False},
    }}
    r = requests.post(url, json=body, headers=_fs_headers(token), timeout=10)
    r.raise_for_status()
    return r.json()["name"].split("/")[-1]

def complete_pending_match(uid: str, doc_id: str, sb_url: str, sb_path: str, token: str):
    doc_path = f"users/{uid}/pendingScreenshots/{doc_id}"
    url = (
        f"{_FS_BASE.format(proj=FIREBASE_PROJECT_ID)}/{doc_path}"
        "?updateMask.fieldPaths=scoreboardUrl"
        "&updateMask.fieldPaths=scoreboardPath"
        "&updateMask.fieldPaths=status"
    )
    body = {"fields": {
        "scoreboardUrl":  {"stringValue": sb_url},
        "scoreboardPath": {"stringValue": sb_path},
        "status":         {"stringValue": "ready"},
    }}
    r = requests.patch(url, json=body, headers=_fs_headers(token), timeout=10)
    r.raise_for_status()

# ── Screenshot capture ─────────────────────────────────────────────────────────

def _grab_jpeg() -> bytes:
    with mss.mss() as sct:
        shot = sct.grab(sct.monitors[1])
        img = Image.frombytes("RGB", shot.size, shot.bgra, "raw", "BGRX")
    buf = io.BytesIO()
    img.save(buf, format="JPEG", quality=85, optimize=True)
    return buf.getvalue()

def _ts_label() -> str:
    return datetime.now().strftime("%H:%M:%S")

def _push_recent(icon: str, text: str):
    _recent.appendleft({"ts": _ts_label(), "icon": icon, "text": text})
    _refresh_recent_ui()

def _set_state(state_text: str, show_cancel: bool):
    if _tk_root is None:
        return
    def _update():
        if _sv_state:  _sv_state.set(state_text)
        if _cancel_btn:
            _cancel_btn.configure(state="normal" if show_cancel else "disabled")
    _tk_root.after(0, _update)

def _refresh_recent_ui():
    if _tk_root is None or _recent_frame is None:
        return
    def _update():
        for w in _recent_frame.winfo_children():
            w.destroy()
        if not _recent:
            ctk.CTkLabel(
                _recent_frame, text="No captures yet this session",
                text_color=FG_MUTED, font=("Segoe UI", 11), anchor="w", justify="left"
            ).pack(anchor="w", padx=16, pady=4)
            return
        for entry in _recent:
            ctk.CTkLabel(
                _recent_frame,
                text=f"{entry['ts']}   {entry['icon']}   {entry['text']}",
                text_color=FG_DIM, font=("Segoe UI", 11), anchor="w", justify="left",
            ).pack(anchor="w", padx=16, pady=2)
    _tk_root.after(0, _update)

def _do_capture():
    """State machine for two-phase capture. Runs in a background thread."""
    global _capture_state, _pending_doc_id, _pending_rank_url

    try:
        token = get_valid_token()
    except Exception as e:
        _push_recent("⚠", f"Auth error: {e}")
        _set_state("⚠️ Not paired", False)
        _show_toast("HeroIntel — Not paired", "Open Settings → Pair desktop.")
        _update_status_indicator()
        return

    uid = _config.get("uid", "")
    ts = int(time.time() * 1000)

    if _capture_state == "idle":
        _set_state("📸 Capturing rank progress...", False)
        try:
            jpeg = _grab_jpeg()
            path = f"users/{uid}/screenshots/{ts}_rank.jpg"
            url = upload_screenshot(jpeg, path, token)
            doc_id = create_pending_match(uid, url, path, token)
            _capture_state  = "waiting_scoreboard"
            _pending_doc_id = doc_id
            _pending_rank_url = url
            _push_recent("📸", "Rank captured — press hotkey on scoreboard")
            _set_state("⏳ Waiting for scoreboard...", True)
            _set_tray_variant("waiting")
            _show_toast("HeroIntel", "Rank captured — press hotkey on the scoreboard.")
        except Exception as e:
            _push_recent("⚠", f"Error: {e}")
            _set_state("⚠️ Ready", False)
            _show_toast("HeroIntel — Error", str(e)[:120])

    elif _capture_state == "waiting_scoreboard":
        _set_state("📸 Capturing scoreboard...", False)
        try:
            jpeg = _grab_jpeg()
            path = f"users/{uid}/screenshots/{ts}_scoreboard.jpg"
            url = upload_screenshot(jpeg, path, token)
            complete_pending_match(uid, _pending_doc_id, url, path, token)
            _capture_state  = "idle"
            _pending_doc_id = ""
            _push_recent("✓", "Match uploaded")
            _set_state("✅ Ready", False)
            _set_tray_variant("idle")
            _show_toast("HeroIntel", "Match captured! Check the web app to review.")
        except Exception as e:
            _push_recent("⚠", f"Error: {e}")
            _set_state("⚠️ Waiting for scoreboard...", True)
            _show_toast("HeroIntel — Error", str(e)[:120])

def on_hotkey():
    threading.Thread(target=_do_capture, daemon=True).start()

def on_cancel_hotkey():
    if _capture_state != "idle":
        cancel_pending()

# ── Cancel pending match ───────────────────────────────────────────────────────

def cancel_pending():
    global _capture_state, _pending_doc_id, _pending_rank_url
    if _capture_state == "idle":
        return
    _capture_state  = "idle"
    _pending_doc_id = ""
    _pending_rank_url = ""
    _push_recent("✕", "Capture cancelled")
    _set_state("✅ Ready", False)
    _set_tray_variant("idle")

