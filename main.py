# ── Tray icon ──────────────────────────────────────────────────────────────────

def _make_icon_image(variant: str = "idle") -> Image.Image:
    path = _resource_path("icon.png")
    if path.exists():
        try:
            img = Image.open(path).convert("RGBA").resize((64, 64), Image.LANCZOS)
            if variant == "waiting":
                overlay = Image.new("RGBA", img.size, (244, 132, 30, 70))
                img = Image.alpha_composite(img, overlay)
            return img
        except Exception as e:
            print(f"[icon] failed to load {path}: {e}")
    # Asset missing — return a flat brand-color square (64×64) so pystray still works.
    color = (244, 132, 30, 255) if variant == "waiting" else (77, 157, 224, 255)
    return Image.new("RGBA", (64, 64), color)

def _set_tray_variant(variant: str):
    if _tray_icon is None:
        return
    try:
        _tray_icon.icon = _make_icon_image(variant)
    except Exception:
        pass

def _open_home(icon=None, _item=None):
    if _tk_root:
        _tk_root.after(0, _tk_root.deiconify)
        _tk_root.after(0, _tk_root.lift)

def _quit(icon, _item):
    icon.stop()
    keyboard.unhook_all()
    os._exit(0)

def _build_tray_menu() -> pystray.Menu:
    return pystray.Menu(
        item("Open", _open_home, default=True, visible=False),
        item(f"Capture ({_current_hotkey.upper()})", lambda i, it: on_hotkey()),
        item("Cancel pending", lambda i, it: cancel_pending(),
             enabled=lambda i: _capture_state != "idle"),
        item("Quit", _quit),
    )

def _rebuild_tray_menu():
    if _tray_icon is None:
        return
    try:
        _tray_icon.menu = _build_tray_menu()
    except Exception:
        pass

# ── Entry point ────────────────────────────────────────────────────────────────

def main():
    global _config
    global _tk_root, _sv_state, _sv_status, _status_label, _recent_frame, _cancel_btn, _sv_instructions
    global _tray_icon, _current_hotkey, _current_cancel_hotkey

    _config = load_config()

    _claim_app_identity()

    _tk_root = ctk.CTk()
    _tk_root.withdraw()

    # First-run: pair via web
    if not _config.get("uid") or not keyring.get_password(KEYRING_SERVICE, KEYRING_REFRESH_KEY):
        paired = run_pairing_dialog(_tk_root)
        if not paired:
            sys.exit(0)
        _config["uid"] = paired["uid"]
        _config.setdefault("hotkey", "f9")
        save_config(_config)

    _current_hotkey = _config.get("hotkey", "f9")
    _current_cancel_hotkey = _config.get("cancel_hotkey", "")

    _sv_state, _sv_status, _status_label, _recent_frame, _cancel_btn, _sv_instructions = build_home_window(
        _tk_root, _current_hotkey)
    _refresh_recent_ui()

    keyboard.add_hotkey(_current_hotkey, on_hotkey, suppress=False)
    if _current_cancel_hotkey:
        try:
            keyboard.add_hotkey(_current_cancel_hotkey, on_cancel_hotkey, suppress=False)
        except Exception as e:
            print(f"[hotkey] cancel hotkey failed: {e}")
            _current_cancel_hotkey = ""

    _tray_icon = pystray.Icon(APP_NAME, _make_icon_image("idle"), "HeroIntel Capture",
                              menu=_build_tray_menu())
    threading.Thread(target=_tray_icon.run, daemon=True).start()

    threading.Thread(target=_status_watcher, daemon=True).start()

    _show_toast("HeroIntel Capture", f"Running — press {_current_hotkey.upper()} after a match.")
    _tk_root.mainloop()


if __name__ == "__main__":
    main()
